package lógica_aula_07;

public class WHILE {

	public static void main(String[] args) {
		// 	MODIFICADOR DE LAÇO

		
		int i = 0;
		
		while ( i < 10) {
			i ++;
			
			if ( i == 3) {
				continue;
			}
			if (i == 9) {
				break;
			}
		System.out.println("Produto: " + i);	
		 	
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
